<template>
   <div class='demo-app'>
    <div class='demo-app-sidebar'>
      <div class='demo-app-sidebar-section'>
        <h2>Instructions</h2>
        <ul>
          <li>Select dates and you will be prompted to create a new event</li>
          <li>Drag, drop, and resize events</li>
          <li>Click an event to delete it</li>
        </ul>
      </div>
      <div class='demo-app-sidebar-section'>
        <label>
          <input
            type='checkbox'
            :checked='calendarOptions.weekends'
            @change='handleWeekendsToggle'
          />
          toggle weekends
        </label>
      </div>
      <div class='demo-app-sidebar-section'>
        <h2>All Events ({{guests.length }})</h2>
        <ul>
          <li v-for='guest in guests' :key='guest.id'>
            <b>{{ guest.startStr }}</b>
            <i>{{ guest.title }}</i>
          </li>
        </ul>
      </div>
    </div>
    <div class='demo-app-main'>
      <FullCalendar
        class='demo-app-calendar'
        :options='calendarOptions'
      >
        <template v-slot:eventContent='arg'>
          <b>{{ arg.timeText }}</b>
          <i>{{ arg.event.title }}</i>
        </template>
      </FullCalendar>
    </div>
  </div>
</template>

<script>
import FullCalendar from '@fullcalendar/vue'
import dayGridPlugin from '@fullcalendar/daygrid'
import timeGridPlugin from '@fullcalendar/timegrid'
import interactionPlugin from '@fullcalendar/interaction'
import { INITIAL_EVENTS, createEventId } from '../event-utils';

const axios = require('axios');

const guestsUrl = 'https://tst-api.feeditback.com/exam.guests';

export default {

  components: {
    FullCalendar // make the <FullCalendar> tag available
  },

  data: function() {
    return {
      calendarOptions: {
        plugins: [
          dayGridPlugin,
          timeGridPlugin,
          interactionPlugin // needed for dateClick
        ],
        headerToolbar: {
          left: 'prev,next today',
          center: 'title',
          right: 'dayGridMonth,timeGridWeek,timeGridDay'
        },
        initialView: 'dayGridMonth',
        initialEvents: INITIAL_EVENTS, // alternatively, use the `events` setting to fetch from a feed
        editable: true,
        selectable: true,
        selectMirror: true,
        dayMaxEvents: true,
        weekends: true,
        select: this.handleDateSelect,
        eventClick: this.handleEventClick,
        eventsSet: this.handleEvents
        /* you can update a remote database when these fire:
        eventAdd:
        eventChange:
        eventRemove:
        */
      },
      guests: []
    }
  },

  created() {
    this.guestsUrl = guestsUrl;

    this.initCalendar(INITIAL_EVENTS);
  }, 

  methods: {

    handleWeekendsToggle() {
      this.calendarOptions.weekends = !this.calendarOptions.weekends // update a property
    },

    handleDateSelect(selectInfo) {
      let title = prompt('Please enter a new title for your event')
      let calendarApi = selectInfo.view.calendar

      calendarApi.unselect() // clear date selection

      if (title) {
        calendarApi.addEvent({
          id: createEventId(),
          title,
          start: selectInfo.startStr,
          end: selectInfo.endStr,
          allDay: selectInfo.allDay
        })
      }
    },

    handleEventClick(clickInfo) {
      if (confirm(`Are you sure you want to delete the event '${clickInfo.event.title}'`)) {
        clickInfo.event.remove()
      }
    },

    handleEvents(guests) {
        console.log("GUESTS",guests);
      this.guests = guests
    },

    initCalendar() {
        this.getGuests();
    },

    
    getGuests() {
       const headers = {
        'X-FIB-API-AUTH-TYPE': 'exam',
        'X-FIB-API-LANGUAGE': 'en_GB',
        'X-FIB-API-AUTH-TOKEN': 'F6HCAFVHPEg3"Sw#'
      }

      axios.get(`${guestsUrl}/`, { params: { offset: false, limit: 15 } },{
        headers: headers
      }).then(response => {
        let guests = response.data;
        console.log("RESPONSE", response.data);
        let apiGuests = guests.map(guest => this.visitingGuest(guest));
        this.calendarOptions.events = [... apiGuests];
      })
      .catch(error => {
        console.log("this is the error", error);
      });

    },

    visitingGuest(guest) { //factory function 
      let singleGuest = {
        id: guest.id,
        title: "Guest",
        start: guest.date,
        allDay: false,
      };
      return singleGuest;
    },
  }
}
</script>


